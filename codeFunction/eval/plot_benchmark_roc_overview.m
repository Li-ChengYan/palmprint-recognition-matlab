function plot_benchmark_roc_overview(results, outputPath, titleText)
%PLOT_BENCHMARK_ROC_OVERVIEW Plot all algorithm ROC curves into one figure.

if nargin < 3
    titleText = "Overall ROC";
end

figureHandle = figure( ...
    'Visible', 'off', ...
    'Color', 'w', ...
    'Name', titleText, ...
    'NumberTitle', 'off', ...
    'Position', [100, 100, 760, 620]);
cleanup = onCleanup(@() close(figureHandle));
axesHandle = axes('Parent', figureHandle);
hold(axesHandle, 'on');

colors = lines(height(results));
lineStyles = ["-", "--", "-.", ":"];
markers = ["o", "s", "^", "d", "v", ">", "<", "p", "h", "x", "+", "*"];
xTicks = [1e-4 1e-3 1e-2 1e-1 1];
yTicks = 0.99:0.001:1.00;

for idx = 1:height(results)
    farRatio = results.far{idx}(:) / 100;
    garRatio = results.gar{idx}(:) / 100;
    style = lineStyles(mod(idx - 1, numel(lineStyles)) + 1);
    marker = markers(mod(idx - 1, numel(markers)) + 1);
    semilogx(axesHandle, farRatio, garRatio, ...
        'LineWidth', 1.6, ...
        'Color', colors(idx, :), ...
        'LineStyle', char(style), ...
        'Marker', char(marker), ...
        'MarkerSize', 6, ...
        'DisplayName', char(results.algorithm(idx)));
end

set(axesHandle, ...
    'Box', 'on', ...
    'FontSize', 11, ...
    'LineWidth', 1.0, ...
    'XScale', 'log', ...
    'XMinorGrid', 'on', ...
    'YMinorGrid', 'on', ...
    'GridAlpha', 0.18, ...
    'MinorGridAlpha', 0.12);

title(axesHandle, titleText);
xlabel(axesHandle, 'FAR');
ylabel(axesHandle, 'GAR');
xlim(axesHandle, [1e-4, 1]);
ylim(axesHandle, [0.99, 1]);
xticks(axesHandle, xTicks);
yticks(axesHandle, yTicks);
legend(axesHandle, 'Location', 'southeast', 'Box', 'on', 'Color', 'white');
grid(axesHandle, 'on');
hold(axesHandle, 'off');

ensure_directory(fileparts(outputPath));
exportgraphics(figureHandle, outputPath, 'Resolution', 200);
savefig(figureHandle, strrep(outputPath, ".png", ".fig"));
end
